uint32_t Read_ADC()
{
	uint32_t adc_val = 0;
	static uint32_t ADCValue[10] = {0,};
	static uint32_t past_time = 0;
	static uint8_t adc_cnt = 0;
	uint32_t sum = 0;
	if(HAL_GetTick() >= past_time + 10 )
	{
		HAL_ADC_Start(&hadc);	//ADC ?ãú?ûë
		if (HAL_ADC_PollForConversion(&hadc, 1000000) == HAL_OK)  //ADCÍ∞? ?ù¥?ÉÅ?óÜ?úºÎ©?
		{
			ADCValue[adc_cnt++] = HAL_ADC_GetValue(&hadc);					//ADCÍ∞íÏùÑ ???û•
		}
		
		past_time = HAL_GetTick();
	}

	if(adc_cnt == 10)
	{
		adc_cnt = 0;
		for(int i =0 ;i < 10;i++)
		{
			sum += ADCValue[i];
		}
		adc_val = sum/10;

		memset(ADCValue,0, 10*4 );

		return adc_val;
	}
	
	return 0;
	
}

